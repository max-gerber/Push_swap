#include "includes/libft.h"

int	ft_atoi(const char *a)
{
	int sign;
	int value;

	sign = 1;
	value = 0;
	while (*a == '\n' || *a == '\t' || *a == ' ' || *a == '\v' || *a == '\f'
			|| *a == '\r')
		a++;
	if (*a == '-')
		sign = -1;
	if (*a == '+' || *a == '-')
		a++;
	while (*a >= 48 && *a <= 57)
		value = value * 10 + (*a++ - 48);
	return (sign * value);
}
